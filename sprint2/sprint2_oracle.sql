-- Drop tables in reverse dependency order (safe re-run)
BEGIN
  FOR t IN (SELECT table_name FROM user_tables
            WHERE table_name IN ('DISPUTES','PAYMENTS','BOOKINGS',
                                 'PARKING_SLOTS','VEHICLES','USERS'))
  LOOP
    EXECUTE IMMEDIATE 'DROP TABLE ' || t.table_name || ' CASCADE CONSTRAINTS';
  END LOOP;
END;
/

-- ============================================================
-- TABLE: USERS
-- Normalization note: All non-key attributes (email, name,
-- phone, role, is_active, timestamps) are fully dependent on
-- user_id only. No partial or transitive dependencies. In 3NF.
-- ============================================================
CREATE TABLE users (
    user_id       NUMBER GENERATED BY DEFAULT AS IDENTITY,
    email         VARCHAR2(255)  NOT NULL,
    password_hash VARCHAR2(255)  NOT NULL,
    first_name    VARCHAR2(100),
    last_name     VARCHAR2(100),
    phone         VARCHAR2(30),
    role          VARCHAR2(20)   DEFAULT 'user' NOT NULL,
    is_active     NUMBER(1)      DEFAULT 1       NOT NULL,
    created_at    DATE           DEFAULT SYSDATE,
    updated_at    DATE           DEFAULT SYSDATE,
    CONSTRAINT pk_users         PRIMARY KEY (user_id),
    CONSTRAINT uq_users_email   UNIQUE (email),
    CONSTRAINT chk_users_role   CHECK (role IN ('user','admin')),
    CONSTRAINT chk_users_active CHECK (is_active IN (0,1))
);

-- ============================================================
-- TABLE: VEHICLES
-- Normalization note: All attributes depend only on vehicle_id.
-- user_id is a FK — no transitive dependency since user data
-- lives in USERS. plate_number is a candidate key. In 3NF.
-- ON DELETE: Restrict (default) — cannot delete user with vehicles.
-- ============================================================
CREATE TABLE vehicles (
    vehicle_id   NUMBER GENERATED BY DEFAULT AS IDENTITY,
    user_id      NUMBER         NOT NULL,
    plate_number VARCHAR2(20)   NOT NULL,
    make         VARCHAR2(50),
    model        VARCHAR2(50),
    color        VARCHAR2(30),
    year         NUMBER(4),
    is_active    NUMBER(1)      DEFAULT 1 NOT NULL,
    created_at   DATE           DEFAULT SYSDATE,
    CONSTRAINT pk_vehicles        PRIMARY KEY (vehicle_id),
    CONSTRAINT uq_plate           UNIQUE (plate_number),
    CONSTRAINT fk_vehicles_user   FOREIGN KEY (user_id) REFERENCES users(user_id),
    CONSTRAINT chk_vehicle_active CHECK (is_active IN (0,1))
);

-- ============================================================
-- TABLE: PARKING_SLOTS
-- Normalization note: slot_code, level_name, zone, status all
-- depend solely on slot_id. No multi-valued or transitive deps.
-- slot_code is a candidate key. In BCNF.
-- ============================================================
CREATE TABLE parking_slots (
    slot_id    NUMBER GENERATED BY DEFAULT AS IDENTITY,
    slot_code  VARCHAR2(30)  NOT NULL,
    level_name VARCHAR2(30),
    zone       VARCHAR2(30),
    status     VARCHAR2(20)  DEFAULT 'active' NOT NULL,
    created_at DATE          DEFAULT SYSDATE,
    CONSTRAINT pk_slots        PRIMARY KEY (slot_id),
    CONSTRAINT uq_slot_code    UNIQUE (slot_code),
    CONSTRAINT chk_slot_status CHECK (status IN ('active','maintenance','closed'))
);

-- ============================================================
-- TABLE: BOOKINGS
-- Normalization note: All attributes depend on booking_id.
-- user_id, vehicle_id, slot_id are FKs — user/vehicle/slot
-- attributes live in their own tables (no transitive deps here).
-- start_time and end_time are independent facts about the booking.
-- In 3NF / BCNF.
-- ON DELETE: Restrict — cannot delete a user or slot with bookings.
-- ============================================================
CREATE TABLE bookings (
    booking_id NUMBER GENERATED BY DEFAULT AS IDENTITY,
    user_id    NUMBER       NOT NULL,
    vehicle_id NUMBER,
    slot_id    NUMBER       NOT NULL,
    start_time DATE         NOT NULL,
    end_time   DATE         NOT NULL,
    status     VARCHAR2(20) DEFAULT 'reserved' NOT NULL,
    created_at DATE         DEFAULT SYSDATE,
    CONSTRAINT pk_bookings         PRIMARY KEY (booking_id),
    CONSTRAINT fk_bookings_user    FOREIGN KEY (user_id)
        REFERENCES users(user_id),
    CONSTRAINT fk_bookings_vehicle FOREIGN KEY (vehicle_id)
        REFERENCES vehicles(vehicle_id),
    CONSTRAINT fk_bookings_slot    FOREIGN KEY (slot_id)
        REFERENCES parking_slots(slot_id),
    CONSTRAINT chk_booking_time    CHECK (end_time > start_time),
    CONSTRAINT chk_booking_status  CHECK (status IN
        ('reserved','active','completed','cancelled','no_show','disputed'))
);

-- ============================================================
-- TABLE: PAYMENTS
-- Normalization note: All payment attributes depend on payment_id.
-- booking_id is a FK and also a candidate key (UNIQUE).
-- amount_cents, currency, status, provider, paid_at are all
-- facts about the payment itself — no transitive deps. In BCNF.
-- ON DELETE: Restrict — cannot delete a booking with a payment.
-- ============================================================
CREATE TABLE payments (
    payment_id   NUMBER GENERATED BY DEFAULT AS IDENTITY,
    booking_id   NUMBER         NOT NULL,
    amount_cents NUMBER         NOT NULL,
    currency     CHAR(3)        DEFAULT 'USD' NOT NULL,
    status       VARCHAR2(20)   DEFAULT 'pending' NOT NULL,
    provider     VARCHAR2(30),
    provider_ref VARCHAR2(100),
    paid_at      DATE,
    created_at   DATE           DEFAULT SYSDATE,
    CONSTRAINT pk_payments         PRIMARY KEY (payment_id),
    CONSTRAINT uq_payment_booking  UNIQUE (booking_id),
    CONSTRAINT fk_payments_booking FOREIGN KEY (booking_id)
        REFERENCES bookings(booking_id),
    CONSTRAINT chk_payment_amount  CHECK (amount_cents > 0),
    CONSTRAINT chk_payment_status  CHECK (status IN
        ('pending','authorized','paid','failed','refunded'))
);

-- ============================================================
-- TABLE: DISPUTES
-- Normalization note: All attributes depend on dispute_id.
-- booking_id, opened_by_user_id, assigned_admin_id are FKs.
-- No multi-valued attributes — description and resolution_note
-- are single CLOB facts per dispute. In BCNF.
-- Two FKs to users represent different roles (no violation).
-- ON DELETE: Restrict — cannot delete booking or user with disputes.
-- ============================================================
CREATE TABLE disputes (
    dispute_id         NUMBER GENERATED BY DEFAULT AS IDENTITY,
    booking_id         NUMBER        NOT NULL,
    opened_by_user_id  NUMBER        NOT NULL,
    assigned_admin_id  NUMBER,
    reason             VARCHAR2(50)  NOT NULL,
    description        CLOB,
    status             VARCHAR2(20)  DEFAULT 'open' NOT NULL,
    resolution_note    CLOB,
    created_at         DATE          DEFAULT SYSDATE,
    resolved_at        DATE,
    CONSTRAINT pk_disputes         PRIMARY KEY (dispute_id),
    CONSTRAINT fk_disputes_booking FOREIGN KEY (booking_id)
        REFERENCES bookings(booking_id),
    CONSTRAINT fk_disputes_opened  FOREIGN KEY (opened_by_user_id)
        REFERENCES users(user_id),
    CONSTRAINT fk_disputes_admin   FOREIGN KEY (assigned_admin_id)
        REFERENCES users(user_id),
    CONSTRAINT chk_dispute_status  CHECK (status IN
        ('open','in_review','resolved','rejected'))
);

