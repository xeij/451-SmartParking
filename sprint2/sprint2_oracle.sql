-- AQ1: Full booking report (user + slot)
SELECT b.booking_id,
       u.first_name || ' ' || u.last_name AS user_name,
       ps.slot_code, ps.level_name, ps.zone,
       b.start_time, b.end_time, b.status
FROM bookings b
JOIN users          u  ON b.user_id = u.user_id
JOIN parking_slots  ps ON b.slot_id  = ps.slot_id
ORDER BY b.start_time DESC;

-- AQ2: Transaction report (booking + vehicle + payment)
SELECT b.booking_id,
       u.email,
       v.plate_number,
       v.make || ' ' || v.model    AS vehicle,
       p.amount_cents / 100        AS amount_usd,
       p.status                    AS payment_status,
       p.paid_at
FROM bookings b
JOIN users    u ON b.user_id    = u.user_id
JOIN vehicles v ON b.vehicle_id = v.vehicle_id
JOIN payments p ON b.booking_id = p.booking_id
ORDER BY p.paid_at DESC NULLS LAST;

-- AQ3: Available slots for a given time window
SELECT ps.slot_id, ps.slot_code, ps.level_name, ps.zone
FROM parking_slots ps
WHERE ps.status = 'active'
  AND ps.slot_id NOT IN (
      SELECT b.slot_id FROM bookings b
      WHERE b.status NOT IN ('cancelled','completed','no_show')
        AND b.start_time < TO_DATE('2025-05-01 12:00','YYYY-MM-DD HH24:MI')
        AND b.end_time   > TO_DATE('2025-05-01 10:00','YYYY-MM-DD HH24:MI')
  )
ORDER BY ps.slot_code;

-- AQ4: Revenue per user (paid bookings only)
SELECT u.user_id,
       u.first_name || ' ' || u.last_name AS user_name,
       COUNT(p.payment_id)       AS total_bookings,
       SUM(p.amount_cents) / 100 AS total_spent_usd
FROM users u
JOIN bookings b ON u.user_id    = b.user_id
JOIN payments p ON b.booking_id = p.booking_id
WHERE p.status = 'paid'
GROUP BY u.user_id, u.first_name, u.last_name
ORDER BY total_spent_usd DESC;

-- AQ5: Dispute dashboard (opener + admin + slot)
SELECT d.dispute_id,
       op.first_name || ' ' || op.last_name  AS opened_by,
       ad.first_name || ' ' || ad.last_name  AS assigned_admin,
       ps.slot_code,
       b.start_time,
       d.reason,
       d.status
FROM disputes d
JOIN bookings      b  ON d.booking_id        = b.booking_id
JOIN users         op ON d.opened_by_user_id = op.user_id
LEFT JOIN users    ad ON d.assigned_admin_id  = ad.user_id
JOIN parking_slots ps ON b.slot_id            = ps.slot_id
ORDER BY d.created_at DESC;

-- AQ6: Most booked slots
SELECT ps.slot_code, ps.level_name, ps.zone,
       COUNT(b.booking_id) AS total_bookings
FROM parking_slots ps
JOIN bookings b ON ps.slot_id = b.slot_id
GROUP BY ps.slot_code, ps.level_name, ps.zone
ORDER BY total_bookings DESC;

-- AQ7: Users with open or in-review disputes
SELECT u.user_id,
       u.first_name || ' ' || u.last_name AS user_name,
       u.email,
       COUNT(d.dispute_id) AS open_disputes
FROM users u
JOIN bookings b ON u.user_id    = b.user_id
JOIN disputes d ON b.booking_id = d.booking_id
WHERE d.status IN ('open','in_review')
GROUP BY u.user_id, u.first_name, u.last_name, u.email
ORDER BY open_disputes DESC;

-- AQ8: Bookings with failed payments
SELECT b.booking_id,
       u.first_name || ' ' || u.last_name AS user_name,
       u.email,
       ps.slot_code,
       b.start_time,
       p.amount_cents / 100 AS amount_usd,
       p.status             AS payment_status
FROM bookings b
JOIN users         u  ON b.user_id    = u.user_id
JOIN parking_slots ps ON b.slot_id    = ps.slot_id
JOIN payments      p  ON b.booking_id = p.booking_id
WHERE p.status = 'failed'
ORDER BY b.start_time;

-- AQ9: Full booking history for user 1
SELECT b.booking_id,
       ps.slot_code, ps.zone,
       v.plate_number,
       b.start_time, b.end_time, b.status,
       p.amount_cents / 100 AS amount_usd,
       p.status             AS payment_status
FROM bookings b
JOIN parking_slots ps ON b.slot_id    = ps.slot_id
JOIN payments      p  ON b.booking_id = p.booking_id
LEFT JOIN vehicles v  ON b.vehicle_id = v.vehicle_id
WHERE b.user_id = 1
ORDER BY b.start_time DESC;

-- AQ10: Daily booking count by zone
SELECT ps.zone,
       TRUNC(b.start_time)  AS booking_date,
       COUNT(b.booking_id)  AS bookings_that_day
FROM bookings b
JOIN parking_slots ps ON b.slot_id = ps.slot_id
WHERE b.status NOT IN ('cancelled','no_show')
GROUP BY ps.zone, TRUNC(b.start_time)
ORDER BY booking_date, ps.zone;

